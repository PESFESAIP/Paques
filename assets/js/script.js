document.addEventListener("DOMContentLoaded", function () {
  // Récupérer le mot cible
  var motCible1 = document.querySelector(".mot-cible1");

  // Ajouter un écouteur d'événement pour le clic sur le mot cible
  motCible1.addEventListener("click", function () {
    // Afficher une alerte
    alert(
      "Créée en 2004, l’association AIDER a pour objectif de trouver des solutions pour répondre aux besoins des familles défavorisées de Madagascar. Grâce aux dons des adhérents, AIDER propose et lance des actions qui répondent aux besoins alimentaires et technologiques des familles malgaches. Sur place, des membres de l’association, conseillent et forment les populations touchées par la pauvreté, dans leur vie quotidienne. Les dons permettent aux animateurs de mettre en place des projets tels que  jardins familiaux, parrainage scolaire, enseignement technologique et professionnel, ferme expérimentale."
    );
  });
});

